<?php

namespace BCM\BreadcrumbBundle\Model;

class Item
{
    protected $path;
    protected $label;

    public function setLabel($label)
    {
        $this->label = $label;
    }

    public function getLabel()
    {
        return $this->label;
    }

    public function setPath($path)
    {
        $this->path = $path;
    }

    public function getPath()
    {
        return $this->path;
    }
}